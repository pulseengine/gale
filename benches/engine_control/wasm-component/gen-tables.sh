#!/usr/bin/env bash
# Generate src/tables.rs from ../src/tables.c so the component uses the SAME
# ignition/fuel maps as the C bench (single source of truth). Re-run if the
# C tables change.
set -euo pipefail
HERE="$(cd "$(dirname "$0")" && pwd)"
python3 - "$HERE/../src/tables.c" > "$HERE/src/tables.rs" <<'PY'
import sys, re
src = open(sys.argv[1]).read()

def grab(ctype):
    # match: const <ctype> name[20][20] = { ... };
    m = re.search(r'const\s+%s\s+(\w+)\s*\[\s*RPM_BINS\s*\]\s*\[\s*LOAD_BINS\s*\]\s*=\s*\{(.*?)\}\s*;' % re.escape(ctype), src, re.S)
    if not m: sys.exit("table not found for "+ctype)
    name, body = m.group(1), m.group(2)
    # rows are { a,b,c,... }
    rows = re.findall(r'\{([^{}]*)\}', body)
    rows = [[int(x) for x in re.findall(r'-?\d+', r)] for r in rows]
    return name, rows

sn, sa = grab("int8_t")
fn, fu = grab("uint16_t")
assert len(sa)==20 and all(len(r)==20 for r in sa), "spark table shape"
assert len(fu)==20 and all(len(r)==20 for r in fu), "fuel table shape"

print("// @generated by gen-tables.sh from ../src/tables.c — do not edit by hand.")
print("#[rustfmt::skip]")
print("static SPARK_ADVANCE_TABLE: [[i8; LOAD_BINS]; RPM_BINS] = [")
for r in sa: print("    [" + ",".join(str(v) for v in r) + "],")
print("];")
print("#[rustfmt::skip]")
print("static FUEL_DURATION_TABLE: [[u16; LOAD_BINS]; RPM_BINS] = [")
for r in fu: print("    [" + ",".join(str(v) for v in r) + "],")
print("];")
PY
echo "wrote $HERE/src/tables.rs"
